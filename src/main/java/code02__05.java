public class code02__05{
    public static void main(String[] args){

        int num1, num2, result;

        num1 = 1500;
        num2 = 23;

        result = num1 + num2;
        System.out.println(num1 + " + " + num2 + " = " + result);

        result = num1 - num2;
        System.out.println(num1 + " - " + num2 + " = " + result);

        result = num1 * num2;
        System.out.println(num1 + " * " + num2 + " = " + result);

        result = num1 / num2;
        System.out.println(num1 + " /  " + num2 + " = " + result);

    }
}